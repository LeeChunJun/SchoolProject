package com.leechunjun.school.bean;

public class Grade {
	private String examTime;
	private String courseNo;
	private String courseName;
	private String examPoint;
	private String examScore;
	private String examAgainScore;
	private String examStandardScore;
	public String getExamTime() {
		return examTime;
	}
	public void setExamTime(String examTime) {
		this.examTime = examTime;
	}
	public String getCourseNo() {
		return courseNo;
	}
	public void setCourseNo(String courseNo) {
		this.courseNo = courseNo;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getExamPoint() {
		return examPoint;
	}
	public void setExamPoint(String examPoint) {
		this.examPoint = examPoint;
	}
	public String getExamScore() {
		return examScore;
	}
	public void setExamScore(String examScore) {
		this.examScore = examScore;
	}
	public String getExamAgainScore() {
		return examAgainScore;
	}
	public void setExamAgainScore(String examAgainScore) {
		this.examAgainScore = examAgainScore;
	}
	public String getExamStandardScore() {
		return examStandardScore;
	}
	public void setExamStandardScore(String examStandardScore) {
		this.examStandardScore = examStandardScore;
	}
	
	
}
